
public class Veiculo {
	private String marca;
	private String modelo;
	private String cor;
	private int ano;
	

	//getters e setters
	
	//marca
	
	void setMarca(String marca) {
		this.marca =  marca;
	}
	
	String getMarca() {
		return this.marca;
	}
	
	//modelo
	
	void setModelo(String modelo) {
		this.modelo =  modelo;
	}
	
	String getModelo() {
		return this.modelo;
	}
	
	//cor
	
	void setCor(String cor) {
		this.cor =  cor;
	}
		
	String getCor() {
		return this.cor;
	}
	
	//ano
	
	void setAno(int ano) {
		this.ano =  ano;
	}
		
	int getAno() {
		return this.ano;
	}
	
	

}
